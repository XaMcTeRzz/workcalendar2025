#!/bin/bash

# Скрипт для автоматичного тестування в CI/CD середовищі

set -e

# Встановлення залежностей
echo "Встановлення залежностей..."
npm ci

# Перевірка типів TypeScript
echo "Перевірка типів TypeScript..."
npx tsc --noEmit

# Запуск тестів
echo "Запуск тестів..."
npm test -- --watchAll=false

# Перевірка лінтером
echo "Перевірка лінтером..."
npm run lint

# Збірка проекту для тестування
echo "Збірка проекту для тестування..."
CI=true npm run build

echo "Всі тести пройдено успішно!" 